/*
什么时候使用静态

两个方面：
因为静态修饰的内容有成员变量和函数。
什么时候定义静态变量（类变量）呢？
答：当对象中出现共享数据时，该数据被静态所修饰
对象中的特有数据要定义成非静态存在于堆内存中（对象内部）。

什么时候定义静态函数呢？
答：当功能内部没有访问到非静态数据（对象的特有数据），
那么该功能可以定义成静态的
*/
class Person
{
	String name;
	
	public static void shouw()
	{
		System.out.println(name+"haha");
	}
}
